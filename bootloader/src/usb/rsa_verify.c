uint8_t rsa_verify(uint8_t *hash, uint8_t *signature) {
    struct bn m, s, t1, t2;
    
    bignum_init(&m);
    bignum_from_bytes(&m, modulus, sizeof(modulus));
    bignum_init(&s);
    bignum_from_bytes(&s, signature, sizeof(modulus));
    bignum_init(&t1);
    bignum_init(&t2);
    // s^3 % m
    bignum_mul(&s, &s, &t1);
    bignum_mod(&t1, &m, &t2);
    bignum_mul(&t2, &s, &t1);
    bignum_mod(&t1, &m, &t2);
    
    uint8_t hash_cmp[32];
    bignum_to_bytes(&t2, hash_cmp, sizeof(hash_cmp));
    
    // compare with signature SHA-256
    uint8_t i, cmp=0;
    for (i=0; i<sizeof(hash_cmp); i++) cmp |= hash[i] ^ hash_cmp[i];

    return cmp;
}


